package ArithmeticOperator;

public class Arithmetic {
	public static void main(String[] args) {
	int value3 = 3;
	int value4 = value3++;
	System.out.println(value3);
	System.out.println(value4);
	System.out.println();
	
	int value5 = 3;
	int value6 = ++value5;
	System.out.println(value5);
	System.out.println(value6);
	}
}
